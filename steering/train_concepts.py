def generate_formatted_prompts(concept_name: str, model_tokenizer=None):
    """Get formatted positive/negative prompt lists for a concept."""
    if concept_name not in CONCEPT_DATA:
        raise ValueError(f"Unknown concept: {concept_name}. Available: {list(CONCEPT_DATA.keys())}")

    data = CONCEPT_DATA[concept_name]
    return data["positive"], data["negative"]


def train_concept(concept_name: str, model=None, tokenizer=None):
    """Train RFM directions for a single concept and save to disk."""
    from neural_controllers import NeuralController
    import torch

    logger.info(f"Training concept: {concept_name}")

    positive, negative = generate_formatted_prompts(concept_name)

    # Create training data
    controller = NeuralController(
        model, tokenizer,
        rfm_iters=8, batch_size=2,
        n_components=5, control_method='rfm',
    )

    # Format prompts
    train_positive = [controller.format_prompt(p) for p in positive[:15]]
    train_negative = [controller.format_prompt(p) for p in negative[:15]]
    val_positive = [controller.format_prompt(p) for p in positive[15:]]
    val_negative = [controller.format_prompt(p) for p in negative[15:]]

    train_data = train_positive + train_negative
    train_labels = [1] * len(train_positive) + [0] * len(train_negative)

    val_data = val_positive + val_negative
    val_labels = [1] * len(val_positive) + [0] * len(val_negative)

    # Train directions
    controller.compute_directions(
        train_data, train_labels,
        val_data, val_labels,
    )

    # Save
    controller.save(concept=concept_name, model_name=MODEL_SHORT, path=str(DIRECTIONS_DIR))
    logger.info(f"Saved directions for '{concept_name}' to {DIRECTIONS_DIR}")


def train_all_concepts():
    """Train all concept directions. Requires GPU and loaded model."""
    import torch
    from transformers import AutoTokenizer, AutoModelForCausalLM, BitsAndBytesConfig

    logger.info(f"Loading {MODEL_ID} in 4-bit for training...")
    quantization_config = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_compute_dtype=torch.float16,
        bnb_4bit_quant_type="nf4",
    )
    tokenizer = AutoTokenizer.from_pretrained(MODEL_ID, token=True)
    model = AutoModelForCausalLM.from_pretrained(
        MODEL_ID,
        quantization_config=quantization_config,
        device_map="auto",
        token=True,
    )

    for concept_name in CONCEPT_DATA:
        train_concept(concept_name, model=model, tokenizer=tokenizer)

    logger.info(f"All {len(CONCEPT_DATA)} concepts trained!")


